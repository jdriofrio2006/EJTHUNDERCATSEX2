public class FuerzaFelina {
    int id;
    String alias;
    String destreza;
    int combate;
    String zona;

    public FuerzaFelina(int id, String alias, String destreza, int combate, String zona) {
        this.id = id;
        this.alias = alias;
        this.destreza = destreza;
        this.combate = combate;
        this.zona = zona;
    }

}
